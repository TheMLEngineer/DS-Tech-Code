
# Float
variable = 3.3

print('type of variable :' , type(variable))

# Type conversion
print('Changing float to int :' , int(variable))
print('Changing float to complex :' , complex(variable))

# underscore can be used as Delimter to Float also
variable = 3_2.3_5
print(variable)

# inf and nan
print(float('NaN'))
print(float('inf'))

